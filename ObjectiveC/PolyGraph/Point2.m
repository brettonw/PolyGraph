#import "Point2.h"
#import "Algorithm2.h"
#import "Constants2.h"

//------------------------------------------------------------------------------
// implementation
//------------------------------------------------------------------------------
@implementation Point2

//------------------------------------------------------------------------------
// private methods
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// properties
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// public methods
//------------------------------------------------------------------------------
+ (Vector2*) subtractLeft:(Point2*)left right:(Point2*)right
{
    return [[Vector2 alloc] initWithX:left.x - right.x Y:left.y - right.y];
}

//------------------------------------------------------------------------------
+ (Point2*) addLeftPoint:(Point2*)left rightVector:(Vector2*)right
{
    return [[Point2 alloc] initWithX:left.x + right.x Y:left.y + right.y];
}

//------------------------------------------------------------------------------
+ (Point2*) addLeftVector:(Vector2*)left rightPoint:(Point2*)right
{
    return [self addLeftPoint:right rightVector:left];
}

//------------------------------------------------------------------------------
+ (Point2*) pointWithX:(CGFloat)x Y:(CGFloat)y
{
    return [[Point2 alloc] initWithX:x Y:y];
}

//------------------------------------------------------------------------------
- (NSString*) description
{
    return [NSString stringWithFormat:@"P %@", [super description]];
}

//------------------------------------------------------------------------------
@end

